import { sortTwo } from "../shared.js";
export function bubbleSort_u64_js(array) {
    for (let i = 0; i < array.length; i++) {
        for (let j = 0; j < array.length - i - 1; j++) {
            sortTwo(array, j + 1, j);
        }
    }
    return array;
}
